CREATE TABLE IF NOT EXISTS "users" (
	"id" UUID NOT NULL UNIQUE,
	"email" VARCHAR(255) NOT NULL,
	"password_hash" TEXT NOT NULL,
	"full_name" TEXT NOT NULL,
	"role" VARCHAR(255) NOT NULL,
	PRIMARY KEY("id")
);




CREATE TABLE IF NOT EXISTS "classes" (
	"id" UUID NOT NULL UNIQUE,
	"user_id" UUID NOT NULL,
	"class_name" TEXT NOT NULL,
	"description" TEXT NOT NULL,
	"class_code" VARCHAR(255) NOT NULL,
	PRIMARY KEY("id")
);




CREATE TABLE IF NOT EXISTS "contest" (
	"id" UUID NOT NULL UNIQUE,
	"class_id" UUID NOT NULL,
	"title" TEXT NOT NULL,
	"access_code" VARCHAR(255) NOT NULL,
	"time_limit" INTEGER NOT NULL,
	"scoring_config" JSONB NOT NULL,
	"status" VARCHAR(255) NOT NULL,
	PRIMARY KEY("id")
);




CREATE TABLE IF NOT EXISTS "exams" (
	"contest_id" UUID NOT NULL UNIQUE,
	"question_id" UUID NOT NULL,
	"original_order" INTEGER ARRAY NOT NULL,
	"group_id" UUID NOT NULL,
	"point_weight" DECIMAL NOT NULL
);




CREATE TABLE IF NOT EXISTS "exam_results" (
	"id" INTEGER NOT NULL UNIQUE GENERATED BY DEFAULT AS IDENTITY,
	"contest_id" UUID NOT NULL,
	"display_order" INTEGER NOT NULL,
	"start_at" TIMESTAMP NOT NULL,
	"total_score" DECIMAL NOT NULL,
	"student_name" TEXT NOT NULL,
	"student_id" UUID NOT NULL,
	PRIMARY KEY("id")
);




CREATE TABLE IF NOT EXISTS "questions" (
	"id" UUID NOT NULL UNIQUE,
	"user_id" UUID NOT NULL,
	"subject_id" TEXT NOT NULL,
	"grade" INTEGER NOT NULL,
	"parent_id" UUID NOT NULL,
	"q_type" VARCHAR(255) NOT NULL,
	"layout_type" VARCHAR(255) NOT NULL,
	"content_tex" TEXT NOT NULL,
	"image" TEXT NOT NULL,
	"solution_tex" TEXT NOT NULL,
	"chapter" TEXT NOT NULL,
	"lesson" TEXT NOT NULL,
	"difficulty" SMALLINT NOT NULL,
	"is_sufflable" BOOLEAN NOT NULL,
	PRIMARY KEY("id")
);




CREATE TABLE IF NOT EXISTS "question_details" (
	"id" INTEGER NOT NULL UNIQUE GENERATED BY DEFAULT AS IDENTITY,
	"question_id" UUID NOT NULL,
	"context_tex" TEXT NOT NULL,
	"is_correct" BOOLEAN NOT NULL,
	"order_index" INTEGER NOT NULL,
	"explaination_tex" TEXT NOT NULL,
	"original_order" INTEGER NOT NULL,
	"is_sufflable" BOOLEAN NOT NULL,
	PRIMARY KEY("id")
);



